  <script>
    const items = Array.from(document.querySelectorAll('.phd-item'));
    const ball = document.getElementById('trackBall');
    const trackWrap = document.getElementById('trackWrap');

    let activeIndex = -1;

    function getBallTargetTop(itemEl) {
      // Position ball at the vertical centre of the item's title
      const wrapTop = trackWrap.getBoundingClientRect().top + window.scrollY;
      const itemTop = itemEl.getBoundingClientRect().top + window.scrollY;
      const titleEl = itemEl.querySelector('.phd-item-title');
      const titleMid = titleEl.offsetHeight / 2;
      return (itemTop - wrapTop) + titleMid - (parseInt(getComputedStyle(document.documentElement).getPropertyValue('--ball-size')) / 2);
    }

    function update() {
      const viewMid = window.scrollY + window.innerHeight * 0.42; // trigger point ~40% down viewport

      let newActive = -1;
      for (let i = items.length - 1; i >= 0; i--) {
        const top = items[i].getBoundingClientRect().top + window.scrollY;
        if (viewMid >= top) { newActive = i; break; }
      }

      // Clamp: never go below last item
      if (newActive === -1 && window.scrollY > 0) newActive = 0;

      if (newActive !== activeIndex) {
        activeIndex = newActive;

        // Toggle active class
        items.forEach((el, i) => el.classList.toggle('is-active', i === activeIndex));

        // Move ball
        if (activeIndex >= 0) {
          ball.classList.add('active');
          ball.style.top = getBallTargetTop(items[activeIndex]) + 'px';
        } else {
          ball.classList.remove('active');
          ball.style.top = '0px';
        }
      }
    }

    // Init ball position at top of first item
    window.addEventListener('load', () => {
      ball.style.top = getBallTargetTop(items[0]) + 'px';
      update();
    });

    window.addEventListener('scroll', update, { passive: true });
    window.addEventListener('resize', update, { passive: true });
  </script>

